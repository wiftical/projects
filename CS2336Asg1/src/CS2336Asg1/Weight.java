package CS2336Asg1;

//weight class
public class Weight {
    //poundsToK  method with parameter lb
    public double poundsToK (double lb)
    {
        //return value divided by (approximately)2.205
        return lb/2.205;
    };
    //KilogramsToP method with parameter kg
    public double KilogramsToP (double kg)
    {
        //return value times (approximately)2.205
        return kg*2.205;
    }
}