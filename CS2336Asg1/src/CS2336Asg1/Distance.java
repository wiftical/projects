package CS2336Asg1;

//distance class
public class Distance {
    //kilometersToM  method with parameter km
    public double kilometersToM (double km)
    {
        //return value times 1000
        return km*1000;
    }
    //metersToK  method with parameter m
    public double metersToK (double m)
    {
        //return value divided by 1000
        return m/1000;
    }
}